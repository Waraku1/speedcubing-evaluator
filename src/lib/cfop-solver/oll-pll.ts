/** solveOLL が返す結果 */
export type OLLResult = {
  /** OLL を完成させる手順。すでに完成していれば空配列。 */
  moves: Move[];
  /** 手数 */
  depth: number;
  /** OLL 完了後のキューブ状態。solvePLL にそのまま渡す。 */
  stateAfter: CubeState;
};

/** solvePLL が返す結果 */
export type PLLResult = {
  /** PLL を完成させる手順。すでに完成していれば空配列。 */
  moves: Move[];
  /** 手数 */
  depth: number;
  /** PLL 完了後のキューブ状態（= 完全に解けた状態）。 */
  stateAfter: CubeState;
};

