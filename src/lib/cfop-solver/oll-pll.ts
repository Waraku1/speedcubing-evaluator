/**
 * OLL パターン識別キーを生成する。
 *
 * 対象の 21 マスについて「色が "U" なら 1、それ以外なら 0」を
 * 連結した 21 文字の文字列を返す。
 *
 * 読み取り順（固定）:
 *   U面 0,1,2,3,4,5,6,7,8
 *   F上段 18,19,20
 *   R上段  9,10,11
 *   B上段 45,46,47
 *   L上段 36,37,38
 */
function makeOLLKey(state: CubeState): string {
  const indices = [
    0, 1, 2, 3, 4, 5, 6, 7, 8,   // U面
    18, 19, 20,                    // F上段
     9, 10, 11,                    // R上段
    45, 46, 47,                    // B上段
    36, 37, 38,                    // L上段
  ];
  return indices.map(i => state[i] === "U" ? "1" : "0").join("");
}

/**
 * PLL パターン識別キーを生成する。
 *
 * OLL 完了後なので U面は全て "U" のため含めない。
 * 側面上段 12 マスの実際の色を連結した 12 文字を返す。
 *
 * 読み取り順（固定）:
 *   F上段 18,19,20
 *   R上段  9,10,11
 *   B上段 45,46,47
 *   L上段 36,37,38
 */
function makePLLKey(state: CubeState): string {
  const indices = [
    18, 19, 20,   // F上段
     9, 10, 11,   // R上段
    45, 46, 47,   // B上段
    36, 37, 38,   // L上段
  ];
  return indices.map(i => state[i]).join("");
}

