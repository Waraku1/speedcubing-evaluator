/**
 * U 面の後付け調整（post-AUF）を計算して返す。
 *
 * PLL 完了後、U面が正しい向きになっていない場合がある。
 * （PLL アルゴリズムが U面の向きを変える場合があるため）
 *
 * 4 種類の AUF を試し、完全に解けた状態になるものを返す。
 * どれもマッチしなければ空配列を返す。
 */
function findPostAUF(state: CubeState): readonly Move[] {
  for (const auf of AUF_MOVES) {
    const s = applyMoves(state, auf as Move[]);
    if (isPLLSolved(s)) return auf;
  }
  return [];
}

// ═══════════════════════════════════════════════════════════════
//  OLL ソルバー
// ═══════════════════════════════════════════════════════════════

/**
 * OLL（Orientation of Last Layer）を解く。
 *
 * ── 処理の流れ ────────────────────────────────────────────────
 *
 *  1. すでに OLL が完成していれば即返す。
 *
 *  2. AUF 候補（U×0, U×1, U×2, U×3）を順番に試す。
 *       各候補を適用した状態で OLL キーを生成する。
 *       テーブルにマッチすれば該当アルゴリズムを取得。
 *
 *  3. 手順を組み立てる:
 *       [AUFで使ったU回転] + [テーブルのアルゴリズム]
 *
 *  4. 組み立てた手順を適用した状態を stateAfter として返す。
 *
 * @param state - F2L 完了後の CubeState
 */
export function solveOLL(state: CubeState): OLLResult {

  // ── ステップ1: すでに完成していれば即返す ─────────────────────
  if (isOLLSolved(state)) {
    return { moves: [], depth: 0, stateAfter: state };
  }

  // ── ステップ2: AUF を試しながらテーブルをルックアップ ──────────
  for (const auf of AUF_MOVES) {
    // AUF を適用した仮状態を作る
    const aufState = applyMoves(state, auf as Move[]);

    // この向きの OLL キーを生成
    const key = makeOLLKey(aufState);

    // テーブルにマッチするか確認
    const alg = OLL_TABLE[key];
    if (alg === undefined) continue;

    // ── ステップ3: 手順を組み立てる ───────────────────────────
    // AUFの手 + OLLアルゴリズム の順に連結
    const moves: Move[] = [...auf, ...alg] as Move[];

    // ── ステップ4: 完成後の状態を計算して返す ─────────────────
    const stateAfter = applyMoves(state, moves);
    return { moves, depth: moves.length, stateAfter };
  }

  // マッチしなかった場合（テーブルに未登録のパターン）
  throw new Error(
    `[solveOLL] 対応する OLL パターンが見つかりませんでした。\n` +
    `OLL キー: ${makeOLLKey(state)}\n` +
    `F2L が正しく完成しているか確認してください。`
  );
}

// ═══════════════════════════════════════════════════════════════
//  PLL ソルバー
// ═══════════════════════════════════════════════════════════════

/**
 * PLL（Permutation of Last Layer）を解く。
 *
 * ── 処理の流れ ────────────────────────────────────────────────
 *
 *  1. すでに PLL が完成していれば即返す。
 *
 *  2. 認識前 AUF（pre-AUF）を試す。
 *       AUF 候補を適用した状態で PLL キーを生成。
 *       テーブルにマッチすれば該当アルゴリズムを取得。
 *
 *  3. アルゴリズムを適用後、U面の向きを確認（post-AUF）。
 *       ずれていれば末尾に U / U2 / U' を追加。
 *
 *  4. 手順を組み立てる:
 *       [pre-AUF] + [テーブルのアルゴリズム] + [post-AUF]
 *
 *  5. 組み立てた手順を適用した状態を stateAfter として返す。
 *
 * @param state - OLL 完了後の CubeState
 */
export function solvePLL(state: CubeState): PLLResult {

  // ── ステップ1: すでに完成していれば即返す ─────────────────────
  if (isPLLSolved(state)) {
    return { moves: [], depth: 0, stateAfter: state };
  }

  // ── ステップ2: pre-AUF を試しながらテーブルをルックアップ ──────
  for (const preAuf of AUF_MOVES) {
    // pre-AUF を適用した仮状態を作る
    const preState = applyMoves(state, preAuf as Move[]);

    // この向きの PLL キーを生成
    const key = makePLLKey(preState);

    // テーブルにマッチするか確認
    const alg = PLL_TABLE[key];
    if (alg === undefined) continue;

    // アルゴリズムを適用した中間状態
    const midState = applyMoves(preState, alg as Move[]);

    // ── ステップ3: post-AUF を計算 ────────────────────────────
    const postAuf = findPostAUF(midState);

    // ── ステップ4: 手順を組み立てる ───────────────────────────
    const moves: Move[] = [...preAuf, ...alg, ...postAuf] as Move[];

    // ── ステップ5: 完成後の状態を計算して返す ─────────────────
    const stateAfter = applyMoves(state, moves);
    return { moves, depth: moves.length, stateAfter };
  }

  // マッチしなかった場合（テーブルに未登録のパターン）
  throw new Error(
    `[solvePLL] 対応する PLL パターンが見つかりませんでした。\n` +
    `PLL キー: ${makePLLKey(state)}\n` +
    `OLL が正しく完成しているか確認してください。`
  );
}