/**
 * oll-pll.ts — CFOP OLL / PLL ソルバー
 *
 * ═══════════════════════════════════════════════════════════════
 *  このファイルの全体像
 * ═══════════════════════════════════════════════════════════════
 *
 *  OLL（Orientation of Last Layer）
 *    上面（U面）の色が全て "U" になるよう向きを揃える。
 *    57 パターンに対応した手順テーブルをルックアップして解く。
 *
 *  PLL（Permutation of Last Layer）
 *    OLL 完了後、上段の各ピースを正しい位置に移動させる。
 *    21 パターンに対応した手順テーブルをルックアップして解く。
 *
 *  両フェーズとも「テーブルルックアップ方式」を採用する。
 *  BFS ではなく、現在の状態がどのパターンに一致するかを
 *  照合するだけなので処理は非常に高速（O(1)〜O(n)）。
 *
 * ═══════════════════════════════════════════════════════════════
 *  AUF（Adjust U Face）とは
 * ═══════════════════════════════════════════════════════════════
 *
 *  OLL/PLL のパターンには「向き」がある。
 *  例えば同じ OLL パターンでも、U 面を 90° 回転させると
 *  別のパターンに見えるが、本質的には同じケースである。
 *
 *  AUF = U / U2 / U' のいずれかをパターン認識の前後に挿入して
 *        テーブルの「標準向き」に合わせる操作。
 *
 *  本実装では:
 *    [認識前 AUF]  状態に U×0, U×1, U×2, U×3 を適用した
 *                  4 通りを試してテーブルにマッチするものを探す。
 *                  マッチした回転数分の U 回転を手順の前に挿入。
 *    [認識後 AUF]  PLL 完了後、U 面が正しい向きになっているか確認。
 *                  ずれていれば U / U2 / U' を末尾に追加。
 *
 * ═══════════════════════════════════════════════════════════════
 *  パターン識別キーの仕組み
 * ═══════════════════════════════════════════════════════════════
 *
 *  キューブの状態（54 文字列）から「特定のマスの色」だけを
 *  抜き出して文字列キーを作り、テーブルと照合する。
 *
 *  スティッカー番号（moves.ts レイアウト準拠）:
 *
 *            ┌──────────┐
 *            │  0  1  2 │  U面
 *            │  3  4  5 │
 *            │  6  7  8 │
 * ┌──────────┼──────────┼──────────┬──────────┐
 * │ 36 37 38 │ 18 19 20 │  9 10 11 │ 45 46 47 │  ← 上段（Uと隣接）
 * │ 39 40 41 │ 21 22 23 │ 12 13 14 │ 48 49 50 │
 * │ 42 43 44 │ 24 25 26 │ 15 16 17 │ 51 52 53 │
 * └──────────┼──────────┼──────────┴──────────┘
 *            │ 27 28 29 │  D面
 *            │ 30 31 32 │
 *            │ 33 34 35 │
 *            └──────────┘
 *
 *  [OLL キー] 21 文字
 *    U面 9マス (0〜8) + F上段 3マス (18,19,20)
 *    + R上段 3マス (9,10,11) + B上段 3マス (45,46,47)
 *    + L上段 3マス (36,37,38)
 *    各マスの色が "U" なら "1"、それ以外なら "0"
 *
 *  [PLL キー] 20 文字
 *    F上段 3マス (18,19,20) + R上段 3マス (9,10,11)
 *    + B上段 3マス (45,46,47) + L上段 3マス (36,37,38)
 *    ※ OLL 完了後は U面は全て "U" なので含めない
 *    各マスの実際の色をそのまま連結
 *
 * ═══════════════════════════════════════════════════════════════
 *  フェーズ連携（状態の引き渡し）
 * ═══════════════════════════════════════════════════════════════
 *
 *  CFOP の正しい順序は必ず以下の通り:
 *
 *   スクランブル済み状態
 *       ↓
 *   solveCross(scrambled)
 *       → CrossResult.stateAfter    ← Cross完了後の状態
 *       ↓
 *   solveF2L(crossResult.stateAfter)
 *       → F2LResult.stateAfter      ← 下2段（Cross+中段4スロット）完了後の状態
 *       ↓  ← OLL はここから受け取る（F2L完了後）
 *   solveOLL(f2lResult.stateAfter)
 *       → OLLResult.stateAfter      ← U面の向き完了後の状態
 *       ↓
 *   solvePLL(ollResult.stateAfter)
 *       → PLLResult.stateAfter      ← 完全に解けた状態
 *
 *  ※ OLL に cross.stateAfter を直接渡すのは誤り。
 *    Cross完了後はF2L（中段4スロット）がまだ揃っていないため、
 *    F2Lを通さずにOLLを実行しても意味をなさない。
 *    必ず F2L → OLL の順を守ること。
 */

